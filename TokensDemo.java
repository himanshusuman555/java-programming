
public class TokensDemo {				// class is a keyword  , TokensDemo is a identifier  
	public static void main(String args[])   //public , static , void  are 3 keywords
	{										// Curly brackets are Separators
		int a =10, b=20;				// int is keyword  , a ,b are identifiers
		int c = a+b;					// int is keyword  , c is identifier
		System.out.println(c);			// + = are operators
	}									// a=10  is a complete literal

}					// Tokens :- 1. keywords
					//			 2. identifiers
					//			 3. Operators
					//			 4. Separators
					//			 5. Literals